// [ math: 수학적인 상수와 함수를 위한 속성과 메서드를 가진 내장객체 (JS에 이미 내장 된)]
// * 함수객체가 아님!! 
// 숫자데이터를 활용해서 수학적인 연산을 해주는 여러가지 메소드를 포함함.

console.log('abs: ', Math.abs(-12));
// Math.abs()  주어진 숫자의 절대값을 반환 함.
// abs=absolute의 약어. 절대적인 값.
// 예시) -1 을 음수를 제외한 절대값인 숫자 1로 바꿔줌

console.log('min: ', Math.min(2, 8));
// 인수로 들어온 숫자데이터들 중에 가장 작은 값 반환

console.log('max: ', Math.max(2, 8));
// 인수로 들어온 숫자데이터들 중에 가장 큰 값 반환

console.log('ceil: ', Math.ceil(3.14));
// 정수단위로 올림 처리 됨

console.log('floor: ', Math.floor(3.14));
// 정수단위로 내림 처리 됨

console.log('round: ', Math.round(3.14));
// 정수단위로 반올림 처리 됨

console.log('random: ', Math.random());
// 랜덤한 숫자(=난수)를 반환




